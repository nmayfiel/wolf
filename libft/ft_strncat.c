#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	while (*s2 != '\0' && (size_t)j < n)
	{
		s1[i] = *s2;
		i++;
		j++;
		s2++;
	}
	s1[i] = '\0';
	return (s1);
}
