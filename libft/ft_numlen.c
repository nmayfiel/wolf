#include <string.h>

size_t		ft_numlen(int n)
{
	size_t		c;

	if (n == 0)
		return (1);
	c = 0;
	if (n < 0)
		c++;
	while (n != 0)
	{
		n /= 10;
		++c;
	}
	return (c);
}
